const pipoca = (tempo) => {
    if (tempo == 10){
        console.log("Prato pronto, bom apetite!!!")
    }else if (tempo < 10){
        console.log("Tempo insuficiente ")
    }else if (tempo > 20 && tempo < 30){
        console.log("A comida queimou.")
    }else if (tempo >= 30){
        console.log("kabumm")
    }else{
        console.log("Prato inexistente")
    }
}   

